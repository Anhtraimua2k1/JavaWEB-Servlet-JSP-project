    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("utf-8");
        response.setContentType("text/html;charset=UTF-8");
//        processRequest(request, response);
        ProductDBContext productDB = new ProductDBContext();
        CategoryDBContext categoryDB = new CategoryDBContext();

        ArrayList<Category> categories = categoryDB.getAllCategories();
        ArrayList<Product> products = productDB.getAllProducts();

        request.setAttribute("products", products);
        request.setAttribute("categories", categories);
        request.getRequestDispatcher("forgotpass.jsp").include(request, response);

    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("utf-8");
        response.setContentType("text/html;charset=UTF-8");
        
        String checkMessage;
        AccountDBContext accDB = new AccountDBContext();
        
        String username = request.getParameter("username");
        String address = request.getParameter("address");
        String phone = request.getParameter("phone");
        
        Account a = accDB.checkAccount(username, phone ,address);
        
        
        if(a != null){
            request.setAttribute("accId", a.getId());
            request.getRequestDispatcher("changepass.jsp").forward(request, response);
            
        }else{
            checkMessage = "Wrong infomation Account";
            request.setAttribute("checkMessage", checkMessage);
            doGet(request, response);
        
        }
        
        
    }

